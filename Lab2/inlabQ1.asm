ORG 00H
	LJMP MEMCPY

ORG 40H
	
MEMCPY:
	
	USING 0
	PUSH PSW
	PUSH AR3
	PUSH AR2
	PUSH AR1
	PUSH AR0
	
	MOV R2, 50H; VALUE OF N
	MOV A,R2; SAVING THE VALUE OF N
	MOV R0 , 51H; VALUE OF READ POINTER
	MOV R1 , 52H; VALUE OF WRITE POINTER
	
	
	;MOVE INTO STACK
	
	LOOP: 
		MOV B, @R0
		MOV R3, B
		PUSH AR3
		INC R0
		INC R1
		DJNZ R2, LOOP
		
	;POP INTO NEW LOCATION
	DEC R1
	MOV R2, A
	LOOP1:
		
		POP AR3
		MOV B,R3
		MOV @R1,B
		DEC R1
		DJNZ R2, LOOP1
		
	POP AR0
	POP AR1
	POP AR2
	POP AR3
	POP PSW
	;RET
	
	STOP: SJMP STOP 	
	END